namespace SdlSharp.Input
{
    /// <summary>
    /// System cursors.
    /// </summary>
    public enum SystemCursor
    {
        /// <summary>
        /// Arrow cursor
        /// </summary>
        Arrow,

        /// <summary>
        /// I-beam cursor
        /// </summary>
        IBeam,

        /// <summary>
        /// Wait cursor
        /// </summary>
        Wait,

        /// <summary>
        /// Crosshair cursor
        /// </summary>
        Crosshair,

        /// <summary>
        /// Wait arrow cursor
        /// </summary>
        WaitArrow,

        /// <summary>
        /// Size NW/SE cursor
        /// </summary>
        SizeNWSE,

        /// <summary>
        /// Size NE/SW cursor
        /// </summary>
        SizeNESW,

        /// <summary>
        /// Size W/E cursor
        /// </summary>
        SizeWE,

        /// <summary>
        /// Size N/S cursor
        /// </summary>
        SizeNS,

        /// <summary>
        /// Size all cursor
        /// </summary>
        SizeAll,

        /// <summary>
        /// No cursor
        /// </summary>
        No,

        /// <summary>
        /// Hand cursor
        /// </summary>
        Hand,

        /// <summary>
        /// Number of system cursors
        /// </summary>
        SystemCursorsCount
    }
}
