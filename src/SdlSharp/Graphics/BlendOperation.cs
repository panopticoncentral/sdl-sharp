namespace SdlSharp.Graphics
{
    /// <summary>
    /// A blend operation.
    /// </summary>
    public enum BlendOperation
    {
        /// <summary>
        /// Add.
        /// </summary>
        Add = 0x1,

        /// <summary>
        /// Subtract.
        /// </summary>
        Subtract = 0x2,

        /// <summary>
        /// Reverse subtract.
        /// </summary>
        RevSubtract = 0x3,

        /// <summary>
        /// Minimum.
        /// </summary>
        Minimum = 0x4,

        /// <summary>
        /// Maximum
        /// </summary>
        Maximum = 0x5
    }
}
