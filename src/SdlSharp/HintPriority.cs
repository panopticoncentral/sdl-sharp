namespace SdlSharp
{
    /// <summary>
    /// The priority of the hint value.
    /// </summary>
    public enum HintPriority
    {
        /// <summary>
        /// Only change the default value of the hint.
        /// </summary>
        Default,

        /// <summary>
        /// Changes the normal values of the hint.
        /// </summary>
        Normal,

        /// <summary>
        /// Overrides all settings of the hint.
        /// </summary>
        Override
    }
}
