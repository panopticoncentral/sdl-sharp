namespace SdlSharp.Sound
{
    /// <summary>
    /// The status of an audio device.
    /// </summary>
    public enum AudioStatus
    {
        /// <summary>
        /// The audio device is stopped.
        /// </summary>
        Stopped,

        /// <summary>
        /// The audio device is playing.
        /// </summary>
        Playing,

        /// <summary>
        /// The audio device is paused;
        /// </summary>
        Paused
    }
}
